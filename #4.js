function plate(num){
  if (num == 1){
     return "Turkey";
   }
   else if (num == 2){
     return "Stuffing";
   }
   else if (num == 3){
     return "Mashed Potatoes";
   }
   else if (num==4){
     return "Pumpkin Pie";
   }
   else if (num==5){
     return "Cranberry Sauce"
   }
   else {
       return "Do we have enough plates?";
   }
 }

